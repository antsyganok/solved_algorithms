"""
Кодовое слово с цифрой

Вы участвуете в тайной миссии, и ваша задача — найти кодовое слово,
содержащее хотя бы одну цифру. У вас есть список сообщений,
и вы должны проверить их по порядку, чтобы найти нужное слово.
Как только вы находите такое слово, его нужно сразу вывести на экран,
чтобы его увидел руководитель миссии. Если ни одно из сообщений
не содержит кодового слова с цифрой, вы сообщаете: «Кодовое слово не найдено»

Считывать данные в этой задаче не нужно, во входных данных будет определена переменная messages,
которая хранит список строк. Содержимое этой переменной вам нужно будет обработать,
чтобы найти кодовое слово.

Важно: Если сообщение содержит несколько слов с цифрами, распечатать нужно только первое из них.

NB: Фишка в том, что нужно первое вывести слово с цифрой, если оно есть,
поэтому нам нужно айти слово с цифрой и вывести его целиком.
"""

# messages = ["Шифр не найден", "Точка сбора на улице Ленина", "Уточнение к миссии 7А"]
messages = ["Шифр не найден", "Точка сбора на улице 8Марта", "Уточнение к миссии 7А"]
# messages = ["Просто текст", "Ещё немного текста", "Совершенно ничего лишнего"]
# messages = ["123abc 456def 789ghi"]
# messages = ["Объект изучен", "Отчёт подготовлен", "Шифр найден Key007"]

if len(messages) == 1 and ' ' in messages[0]:
    messages = messages[0].split()

for text in messages:
    for word in text.split() if ' ' in text else [text]:
        if any(char.isdigit() for char in word):
            print(word)
            break
    else:
        continue
    break
else:
    print('Кодовое слово не найдено')
