

def selectionSort(data: list[int]) -> list[int]:
    """
    Сортирует массив целых чисел методом наивного выбора с извлечением (pop).

    Алгоритм последовательно находит минимальные элементы, извлекает их из копии
    исходного списка с помощью .pop() и формирует новый упорядоченный массив.
    Оригинальный список при этом не изменяется.

    Особенности реализации:
    - Стабильность: ДА (в отличие от классического Selection Sort, сохраняет
      исходный порядок равных элементов).
    - Временная сложность: O(n^3) в худшем/среднем случае на списках Python,
      так как метод .pop(index) из середины структуры выполняет сдвиг памяти за O(n).
    - Пространственная сложность: O(n) — требует выделения дополнительной памяти.

    Рекомендация: использовать только в учебных целях или на микро-массивах.
    """
    numbers = data.copy()
    new_numbers = []

    while len(numbers) > 0:
        minval = 0
        for i in range(1, len(numbers)):
            if numbers[i] < numbers[minval]:
                minval = i

        minnum = numbers.pop(minval)
        new_numbers.append(minnum)

    return new_numbers

if __name__ == "__main__":
    arr_numbers = [42, 17, 3, 88, 5, 72, 10, 3, 56, 29]
    print(selectionSort(arr_numbers))
